import inspect


class Car:
    """–ü—Ä–æ—Å—Ç–∏–π –∫–ª–∞—Å –¥–ª—è –ø—Ä–∏–∫–ª–∞–¥—É"""

    def __init__(self, brand, year):
        self.brand = brand
        self.year = year


car = Car("Tesla", 2022)

# –Ü–Ω—Ç—Ä–æ—Å–ø–µ–∫—Ü—ñ—è
print(type(car))  # <class '__main__.Car'>
print(car.__dict__)  # {'brand': 'Tesla', 'year': 2022}
print(inspect.getmembers(car))  # —É—Å—ñ –∞—Ç—Ä–∏–±—É—Ç–∏ —Ç–∞ –º–µ—Ç–æ–¥–∏

# –†–µ—Ñ–ª–µ–∫—Å—ñ—è
